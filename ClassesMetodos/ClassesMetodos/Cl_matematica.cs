using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ClassesMetodos
{
    class Cl_matematica
    {
        public int adicao(int parcela1, int parcela2)
        {
            return parcela1 + parcela2;
        }

        public int subratacao(int parcela1, int parcela2)
        {
            return parcela1 - parcela2;
        }

        public int mutiplicacao(int parcela1, int parcela2)
        {
            return parcela1 * parcela2;
        }

        public int divisao(int parcela1 , int parcela2)
        {
            return parcela1 / parcela2;
        }
    }
}
